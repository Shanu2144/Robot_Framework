
#FETCH SUBSTRING BY GIVING INDEX VALUE

email='facebook.com'
print(email[0])

#START AND END INDEX BOTH
print(email[0:8])

#FETCH SUSBSTRING BY GIVING STARTING
print(email[8:])

#FETCH SUSBSTRING BY GIVING END INDEX ONLY
print(email[:8])

#TO FIND LENGTH
print(len(email))

#WANT TO CONVERT MY STRING INTO UPPER CASE
print(email.upper())

#CONVERT OUR STRING INTO LOWER CASE
print(email.lower())

#SET FIRST LETTER IS CAPITAL
print(email.capitalize())

